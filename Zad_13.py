# Получение двух чисел в двоичной системе от пользователя
bin_num1 = input("Введите первое число в двоичной системе: ")
bin_num2 = input("Введите второе число в двоичной системе: ")

# Перевод двоичных чисел в десятичные
dec_num1 = int(bin_num1, 2)
dec_num2 = int(bin_num2, 2)

# Складываем числа в десятичной системе
sum_dec = dec_num1 + dec_num2

# Перевод суммы обратно в двоичную систему
sum_bin = bin(sum_dec)[2:]  # удаляем префикс '0b'

# Вывод результата
print("Сумма в двоичной системе:", sum_bin)